"use client";

import { useState, useEffect } from 'react';
import { useTheme } from '@/contexts/ThemeContext';

export default function FloatingButtons() {
  const [showScrollTop, setShowScrollTop] = useState(false);
  const { isDarkMode } = useTheme();

  useEffect(() => {
    const handleScroll = () => {
      setShowScrollTop(window.scrollY > 300);
    };

    window.addEventListener('scroll', handleScroll);
    return () => window.removeEventListener('scroll', handleScroll);
  }, []);

  const scrollToTop = () => {
    window.scrollTo({
      top: 0,
      behavior: 'smooth'
    });
  };

  const openWhatsApp = () => {
    // استبدل هذا الرقم برقم واتساب الخاص بك
    const phoneNumber = '201062097359'; // رقم واتساب الجديد
    const message = encodeURIComponent('مرحباً، أريد الاستفسار عن منصة حصتي');
    window.open(`https://wa.me/${phoneNumber}?text=${message}`, '_blank');
  };

  return (
    <>
      {/* زر واتساب */}
      <button
        onClick={openWhatsApp}
        className={`fixed left-4 bottom-4 md:left-6 md:bottom-6 z-50 group transition-all duration-300 hover:scale-110`}
        aria-label="تواصل معنا عبر واتساب"
      >
        <div className="relative">
          {/* الدائرة الخلفية */}
          <div className="absolute inset-0 bg-green-500 rounded-full animate-ping opacity-75"></div>
          {/* الزر الفعلي بدون خلفية بيضاء وبدون ظل */}
          <div className="relative rounded-full p-3 md:p-4 transition-colors duration-300 bg-green-500 hover:bg-green-600 text-white">
            <svg 
              className="w-6 h-6 md:w-7 md:h-7" 
              fill="currentColor" 
              viewBox="0 0 24 24"
            >
              <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413Z"/>
            </svg>
          </div>
          {/* النص الظاهر عند التحويم */}
          <div className={`hidden md:block absolute left-full ml-3 top-1/2 -translate-y-1/2 whitespace-nowrap opacity-0 group-hover:opacity-100 transition-opacity duration-300 pointer-events-none ${
            isDarkMode ? 'bg-gray-800 text-white' : 'bg-white text-gray-800'
          } px-3 py-2 rounded-lg shadow-lg text-sm`}>
            تواصل معنا
            <div className={`absolute right-full top-1/2 -translate-y-1/2 border-8 border-transparent ${
              isDarkMode ? 'border-r-gray-800' : 'border-r-white'
            }`}></div>
          </div>
        </div>
      </button>

      {/* زر العودة للأعلى */}
      <div className={`fixed left-4 bottom-20 md:left-6 md:bottom-28 z-50 transition-all duration-500 ${
        showScrollTop 
          ? 'opacity-100 translate-y-0 scale-100' 
          : 'opacity-0 translate-y-10 scale-75 pointer-events-none'
      }`}>
        <button
          onClick={scrollToTop}
          className={`group transition-all duration-300 hover:scale-110 ${
            isDarkMode 
              ? 'bg-primary-600 hover:bg-primary-700 shadow-lg shadow-primary-500/50' 
              : 'bg-primary-600 hover:bg-primary-700 shadow-lg'
          } text-white rounded-full p-3 md:p-4 animate-bounce-slow`}
          aria-label="العودة للأعلى"
        >
          <svg 
            className="w-5 h-5 md:w-6 md:h-6" 
            fill="none" 
            stroke="currentColor" 
            viewBox="0 0 24 24"
          >
            <path 
              strokeLinecap="round" 
              strokeLinejoin="round" 
              strokeWidth={2} 
              d="M5 10l7-7m0 0l7 7m-7-7v18"
            />
          </svg>

          {/* النص الظاهر عند التحويم */}
          <div className={`hidden md:block absolute left-full ml-3 top-1/2 -translate-y-1/2 whitespace-nowrap opacity-0 group-hover:opacity-100 transition-opacity duration-300 pointer-events-none ${
            isDarkMode ? 'bg-gray-800 text-white' : 'bg-white text-gray-800'
          } px-3 py-2 rounded-lg shadow-lg text-sm`}>
            العودة للأعلى
            <div className={`absolute right-full top-1/2 -translate-y-1/2 border-8 border-transparent ${
              isDarkMode ? 'border-r-gray-800' : 'border-r-white'
            }`}></div>
          </div>
        </button>
      </div>
    </>
  );
}
